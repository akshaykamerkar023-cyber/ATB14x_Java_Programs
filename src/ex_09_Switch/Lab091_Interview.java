package ex_09_Switch;

public class Lab091_Interview {
    public static void main(String[] args) {
        long a11 = 302;
            switch ((int) a11) {
                case 1:
                    System.out.println("a11 is 1st");
                    break;
                    case 2:
                        System.out.println("a11 is 2nd");
                        break;
                default :
                    System.out.println("a11 is 3rd");

            }
    }
}
